
## Using the math Module

import math

print(math.sqrt(16))        # Square root of 16
print(math.pow(2, 5))       # 2 raised to the power 5
print(math.pi)              # Value of π (pi)
print(math.floor(4.7))      # Round down to nearest integer
print(math.ceil(4.1))       # Round up to nearest integer


## Using the random Module

import random

print(random.randint(1, 10))   # Random number between 1–10
print(random.choice(['apple', 'banana', 'cherry']))  # Random choice from list
print(random.random())         # Random float between 0.0 to 1.0
